
#include "Engine.h"

/*------------------------------------------------------------------------------
< Texture Pathes >
------------------------------------------------------------------------------*/
#define TEXTURE_TILE_SETS _T("assets/tilesets.png")


/*------------------------------------------------------------------------------
< Constructor >
------------------------------------------------------------------------------*/
Ground::Ground() {
	this->transform = new Transform();
	this->sprite = new Sprite(384,192);
}


/*------------------------------------------------------------------------------
< Destructor >
------------------------------------------------------------------------------*/
Ground::~Ground() {
	delete this->transform;
	delete this->sprite;
}


/*------------------------------------------------------------------------------
< Start >
------------------------------------------------------------------------------*/
void Ground::Start() {
	this->sprite->CreatTexture(TEXTURE_TILE_SETS);
	this->sprite->MakeSlice("Floor",160,64,32,32);
	this->sprite->SetTexture(this->vertex, "Floor");
}


/*------------------------------------------------------------------------------
< Update >
------------------------------------------------------------------------------*/
void Ground::Update() {
	// Update Transform
	this->transform->Update(this->vertex, this->sprite->slices.at("Floor").size);
}


/*------------------------------------------------------------------------------
< Draw >
------------------------------------------------------------------------------*/
void Ground::Draw() {
	this->sprite->Draw(this->vertex);
}
